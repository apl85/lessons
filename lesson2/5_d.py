# D. Вывести цены пяти самых дорогих товаров. Сможете ли вывести цены этих товаров по
# возрастанию, написав минимум кода?
prices = [157.82, 463.51, 9, 5, 55, 66, 77.71, 77.66, 22.44, 65.77, 234.66, 23424.41, 234.23, 23.66, 76.73, 34.23, 345]
prices.sort()
prices.reverse()
for i in prices[0:5]:
    result_rub = int(i)
    result_kop = (i - result_rub) * 100
    top5 = f' {result_rub} руб  {result_kop:02.0f} коп'
    print(top5)
